    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int qtd = Integer.valueOf(quantidades_disp.getSelectedItem().toString());
        String animal = String.valueOf(lista_animais.getSelectedItem());
        String produto = lista_produto.getSelectedItem().toString();
        loja.getProduto(produto, animal).setQuantidade(-qtd);
        loja.setControle("Vendeu-se " + qtd + " unidades de " + produto + " de " + animal + ", valor :" + valor_final.getText());
    }//GEN-LAST:event_jButton1ActionPerformed

    private void quantidades_dispActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_quantidades_dispActionPerformed
        // TODO add your handling code here:
        String animal = String.valueOf(lista_animais.getSelectedItem());
        String produto = lista_produto.getSelectedItem().toString();
        double valor_final1 = Integer.valueOf(quantidades_disp.getSelectedItem().toString()) * loja.getProduto(produto, animal).getPreco();//trocar para valor do produto
        
        valor_final.setText("R$" + String.format(" %.2f",valor_final1));
 
    }//GEN-LAST:event_quantidades_dispActionPerformed

