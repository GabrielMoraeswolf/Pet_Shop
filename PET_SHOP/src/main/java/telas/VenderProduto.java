    private void lista_produtoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lista_produtoActionPerformed
        // TODO add your handling code here:
        // Ao selecionar produto
        String produto_selecionado = lista_produto.getSelectedItem().toString();
        String animal_selecionado = lista_animais.getSelectedItem().toString();
        Produto p = loja.getProduto(produto_selecionado, animal_selecionado);
        quantidade_disponivel.setText(String.valueOf(p.getQuantidade()));
        preco_produto.setText("R$" + String.format(" %.2f",p.getPreco()));
        int qtd = p.getQuantidade();// qtd do produto disponivel
        String quantias[] = new String[qtd];
        for(int i = 0; i < qtd; i++){
            quantias[i] = String.valueOf(i+1);
        }
        quantidades_disp.setModel(new javax.swing.DefaultComboBoxModel<>(quantias));
    }//GEN-LAST:event_lista_produtoActionPerformed

